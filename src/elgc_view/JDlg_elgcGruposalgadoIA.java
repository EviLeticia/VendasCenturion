     public ElgcGruposalgado viewBean() {
        elgcGruposalgado = new ElgcGruposalgado();
        
        elgcGruposalgado.setIdelgcGruposalgado(Util.strInt(jTxtElgcCodigo.getText()));
        elgcGruposalgado.setElgcTiposalgado(jTxtElgcTiposalgado.getText());
        elgcGruposalgado.setElgcTipomassa(jTxtElgcTipomassa.getText());
        elgcGruposalgado.setElgcTipocozimento(jTxtElgcTipocozimento.getText());
        elgcGruposalgado.setElgcRecheio(jCboElgcRecheio.getSelectedIndex());
        elgcGruposalgado.setElgcQuantidade(Util.strDouble(jTxtElgcQuantidade.getText()));
        return elgcGruposalgado;
    }
     
     public void beanView(ElgcGruposalgado elgcGruposalgado){
        jTxtElgcCodigo.setText(Util.intStr(elgcGruposalgado.getIdelgcGruposalgado()));     
        jTxtElgcTiposalgado.setText(elgcGruposalgado.getElgcTiposalgado());
        jTxtElgcTipomassa.setText(elgcGruposalgado.getElgcTipomassa());
        jTxtElgcTipocozimento.setText(elgcGruposalgado.getElgcTipocozimento());
        jCboElgcRecheio.setSelectedIndex(elgcGruposalgado.getElgcRecheio());
        jTxtElgcQuantidade.setText(Util.doubleStr(elgcGruposalgado.getElgcQuantidade()));
     }

