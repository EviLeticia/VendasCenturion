    private void jBtnElgcConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnElgcConfirmarActionPerformed
        elgcVendas = viewBean();
        elgc_Vendas_DAO = new Elgc_Vendas_DAO();

        if (incluindo == true) {
            elgc_Vendas_DAO.insert(elgcVendas);
            elgc_VendasProduto_DAO = new Elgc_VendasProduto_DAO();
            ElgcVendasProduto elgcVendasProduto;
            for (int linha = 0; linha < jTable1.getRowCount(); linha++) {
                elgcVendasProduto = elgc_VendasProdutoControle.getBean(linha);
                elgcVendasProduto.setElgcVendas(elgcVendas);
                elgc_VendasProduto_DAO.insert(elgcVendasProduto);

            }

        } else {
            elgc_Vendas_DAO.update(elgcVendas);
            elgc_VendasProduto_DAO = new Elgc_VendasProduto_DAO();
            ElgcVendasProduto elgcVendasProduto;
            for (int linha = 0; linha < jTable1.getRowCount(); linha++) {
                elgcVendasProduto = elgc_VendasProdutoControle.getBean(linha);
                elgcVendasProduto.setElgcVendas(elgcVendas);
                elgc_VendasProduto_DAO.insert(elgcVendasProduto);
            }

            Util.habilitar(false, jBtnElgcExcluir, jBtnElgcIncluir, jBtnElgcAlterar, jBtnElgcAlterar);
            Util.limparCampos(jTxtElgcNumvendas, jCboElgcUsuario, jFmtElgcDataVenda, jCboElgcCliente, jTxtElgcTotal, jTable1);
            elgc_VendasProdutoControle.setList(new ArrayList());
        }
    }//GEN-LAST:event_jBtnElgcConfirmarActionPerformed

    private void jBtnElgcExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnElgcExcluirActionPerformed
        if (elgcVendas != null) {
            if (Util.perguntar("Deseja excluir a Compra ?") == true) {
                elgc_VendasProduto_DAO = new Elgc_VendasProduto_DAO();
                ElgcVendasProduto elgcVendasProduto;
                for (int linha = 0; linha < jTable1.getRowCount(); linha++) {
                    elgcVendasProduto = elgc_VendasProdutoControle.getBean(linha);
                    elgc_VendasProduto_DAO.delete(elgcVendasProduto);
                }
                elgc_VendasProduto_DAO.delete(elgcVendas);

            }
        } else {
            Util.mensagem("Deve ser realizada uma pesquisa antes");
        }
        Util.limparCampos(jTxtElgcNumvendas, jCboElgcUsuario, jFmtElgcDataVenda, jCboElgcCliente, jTxtElgcTotal, jTable1);

    }//GEN-LAST:event_jBtnElgcExcluirActionPerformed

    private void jBtnElgcAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnElgcAlterarActionPerformed
        Util.habilitar(false, jBtnElgcIncluir, jBtnElgcAlterar, jBtnElgcExcluir, jBtnElgcPesquisar);
        if (elgcVendas != null) {

            elgc_VendasProduto_DAO = new Elgc_VendasProduto_DAO();
            ElgcVendasProduto elgcVendasProduto;
            for (int linha = 0; linha < jTable1.getRowCount(); linha++) {
                elgcVendasProduto = elgc_VendasProdutoControle.getBean(linha);
                elgc_VendasProduto_DAO.delete(elgcVendasProduto);
            }

            Util.habilitar(true, jTxtElgcNumvendas, jTxtElgcData, jCboElgcCliente, jCboElgcUsuario, jTxtElgcTotal, jBtnElgcConfirmar, jBtnElgcCancelar, jBtnElgcIncluirIMG, JBtnElgcAlterarIMG, JBtnElgcExcluirIMG, jTable1);

        } else {
            Util.mensagem("Deve ser realizada uma pesquisa antes");
        }
        incluindo = false;
    }//GEN-LAST:event_jBtnElgcAlterarActionPerformed

