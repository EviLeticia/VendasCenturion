    public ElgcVendas viewBean() {

        elgcVendas = new ElgcVendas();

        elgcVendas.setIdelgcVendas(Util.strInt(jTxtElgcNumvendas.getText()));

        elgcVendas.setElgcCliente((ElgcCliente) jCboElgcUsuario.getSelectedItem());
        elgcVendas.setElgcUsuarios((ElgcUsuarios) jCboElgcCliente.getSelectedItem());
        elgcVendas.setElgcDataVenda(Util.strDate(jFmtElgcDataVenda.getText()));
        elgcVendas.setElgcValortotalVenda(Util.strDouble(jTxtElgcTotal.getText()));

        return elgcVendas;

    }

    public void beanView(ElgcVendas elgcVendas) {

        jTxtElgcNumvendas.setText(Util.intStr(elgcVendas.getIdelgcVendas()));
        jCboElgcUsuario.setSelectedItem(elgcVendas.getElgcUsuarios());
        jCboElgcCliente.setSelectedItem((elgcVendas.getElgcCliente()));
        jFmtElgcDataVenda.setText(Util.datestr(elgcVendas.getElgcDataVenda()));
        jTxtElgcTotal.setText(Util.doubleStr(elgcVendas.getElgcValortotalVenda()));

        elgc_VendasProduto_DAO = new Elgc_VendasProduto_DAO();
        List listaProdutos = (List) elgc_VendasProduto_DAO.listProdutos(elgcVendas);

        elgc_VendasProdutoControle.setList(listaProdutos);

    }

